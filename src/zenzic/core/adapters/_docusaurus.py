"""DocusaurusAdapter — adapter for Docusaurus with native i18n support.

Docusaurus uses a fundamentally different geography from MkDocs:

- Source files live in ``docs/`` with ``.md`` or ``.mdx`` extensions.
- Translations live in ``i18n/{locale}/docusaurus-plugin-content-docs/current/``.
- The sidebar can be auto-generated from the filesystem or declared explicitly
  in ``sidebars.ts``.
- Frontmatter ``slug:`` overrides the canonical URL derived from the filepath.
- Clean URLs are the default: ``docs/guide/install.mdx`` → ``/docs/guide/install``.

Config parsing strategy (approved by Tech Lead):

- **Structural parameters** (locales, docs_dir, fallback) are read from
  ``.zenzic.toml`` via ``BuildContext`` — same as ``ZensicalAdapter``.
- **``baseUrl``** is extracted from ``docusaurus.config.ts`` or ``.js`` via a
  multi-pattern static parser (no Node.js subprocess — Pillar 2 compliance).
- **``routeBasePath``** is extracted from presets (e.g. ``@docusaurus/preset-classic``)
  when present.
- **``slug``** in frontmatter overrides the canonical URL in ``_map_url()``.
- **Sidebar** in ``autogenerated`` mode: all files are ``REACHABLE``.

When the config uses dynamic patterns that defeat static analysis (async
functions, dynamic imports, template literals), the parser falls back to
``'/'`` with a warning — never crashes, never guesses.

This module never imports or executes JavaScript.
"""

