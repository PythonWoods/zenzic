"""BaseAdapter Protocol — the engine-agnostic contract every adapter must satisfy.

The protocol defines two routing APIs:

1. ``get_route_info(rel) → RouteMetadata`` — the **Metadata-Driven** API
   introduced in v0.6.0a1.  Returns all routing metadata in a single call:
   canonical URL, route status, optional slug, route base path, and proxy flag.
2. ``map_url(rel) → str`` and ``classify_route(rel, nav_paths) → RouteStatus``
   — the **legacy File-to-URL** API, preserved for backward compatibility.
   Default implementations delegate to ``get_route_info()``.

Third-party adapters may implement either surface; the core prefers
``get_route_info()`` when available.
"""

from __future__ import annotations

from dataclasses import dataclass
from pathlib import Path
from typing import TYPE_CHECKING, Literal, Protocol, runtime_checkable


if TYPE_CHECKING:
    from zenzic.models.vsm import RouteStatus


# ── RouteMetadata ─────────────────────────────────────────────────────────────


@dataclass(frozen=True, slots=True)
class ContentRoot:
    """A documentation content root scanned in addition to ``docs_dir``.

    Introduced in EPOCH 7a (Multi-Root Discovery) to fix the *VSM Blindness*
    bug: prior to v0.7.1, the VSM only ingested files under ``docs_dir``,
    so plugin-managed content trees living elsewhere — e.g. Docusaurus's
    ``blog/`` directory — were silently excluded from validation.  The
    Docusaurus build then surfaced broken links that ``zenzic check all``
    had missed.

    Adapters expose extra content roots through the optional method
    ``get_extra_content_roots(repo_root) -> list[ContentRoot]``.  The Core
    discovers the method via ``hasattr()`` (matching the pre-existing
    :meth:`BaseAdapter.get_locale_source_roots` convention) so that
    third-party adapters opt in only when ready — no breaking change for
    adapters built against the v0.7.0 Protocol.

    The Core then loads every Markdown file under each root and feeds them
    to the VSM with the declared ``url_prefix`` injected at the front of
    the relative path — so ``blog/2026-04-12-foo.mdx`` becomes
    ``Path('blog/2026-04-12-foo.mdx')`` when the prefix is ``'blog'`` and
    the root is the repo's ``blog/`` directory.

    Attributes:
        path:       Absolute path to the content root (e.g. ``<repo>/blog``).
        url_prefix: URL prefix the engine prepends when routing files under
                    this root (e.g. ``'blog'`` for Docusaurus default).  Empty
                    string means files are served at the site root.  The
                    prefix is also injected as the first ``rel`` segment when
                    the file is dispatched to ``get_route_info()`` so the
                    adapter can disambiguate.
        label:      Human-readable identifier for diagnostics
                    (e.g. ``'docusaurus-blog'``, ``'mkdocs-material-blog'``).
    """

    path: Path
    url_prefix: str
    label: str


# ── VirtualRoute ─────────────────────────────────────────────────────────────

VirtualRouteKind = Literal["tag", "tag_index", "pagination", "author", "author_index"]


@dataclass(frozen=True, slots=True)
class VirtualRoute:
    """A URL route generated by the build engine with no physical Markdown source.

    Introduced in EPOCH 7b (Virtual Frontier) to solve *Virtual Route Blindness*:
    prior to v0.7.2, the VSM had no knowledge of engine-generated pages such as
    tag listing pages, paginated blog indexes, or author profiles.  Any link
    pointing at these URLs was incorrectly flagged as a broken link.

    The **Reverse-Mapping Invariant** requires that every VirtualRoute carries a
    non-empty ``source_files`` set that traces it back to the physical Markdown
    files whose frontmatter metadata caused the route to be generated.  This
    invariant is enforced at construction time via ``__post_init__``.

    Adapters expose virtual routes through the optional method
    ``get_virtual_routes(md_contents) -> list[VirtualRoute]``.  The Core
    discovers the method via ``hasattr()`` (same pattern as
    ``get_extra_content_roots``) so that third-party adapters opt in only when
    ready — no breaking change.

    Attributes:
        url:          Canonical URL path (e.g. ``/blog/tags/python/``).
        label:        Human-readable label for diagnostics
                      (e.g. ``'tag:python'``, ``'tag_index'``).
        source_files: Repo-relative POSIX paths of the physical Markdown files
                      that caused this route to be generated (Reverse-Mapping).
                      Must be non-empty — raises ``ValueError`` otherwise.
        kind:         Machine-readable route type for CLI export and external
                      tool consumption.
    """

    url: str
    label: str
    source_files: frozenset[str]
    kind: VirtualRouteKind

    def __post_init__(self) -> None:
        if not self.source_files:
            raise ValueError(
                f"VirtualRoute {self.url!r}: source_files is empty — "
                "Reverse-Mapping Invariant violated"
            )


@dataclass(slots=True)
class RouteMetadata:
    """Unified routing metadata for a single source file.

    Centralises all information that the VSM builder needs to create a
    :class:`~zenzic.models.vsm.Route` — canonical URL, reachability status,
    slug override, route-base-path prefix, and proxy flag.

    Attributes:
        canonical_url: The URL path the build engine will serve for this file
            (e.g. ``'/guide/install/'``).
        status: Routing status (``REACHABLE``, ``ORPHAN_BUT_EXISTING``,
            ``IGNORED``, or ``CONFLICT``).
        slug: Optional frontmatter ``slug:`` override.  ``None`` when the
            URL is derived purely from the filesystem path.
        route_base_path: URL prefix from the docs plugin preset
            (e.g. ``'docs'`` for Docusaurus default, ``''`` for root-served
            docs).  Defaults to ``'/'`` (no prefix).
        is_proxy: ``True`` for routes that are generated by the build engine
            at build time and have no physical source file (e.g. i18n index
            pages auto-created by ``reconfigure_material`` or Docusaurus's
            i18n entry points).  Proxy routes are ``REACHABLE`` but should
            not be checked for content integrity.
        version: Optional version identifier for multi-version setups (e.g. Docusaurus versioned_docs).
    """

    canonical_url: str
    status: RouteStatus
    slug: str | None = None
    route_base_path: str = "/"
    is_proxy: bool = False
    version: str | None = None


# ── BaseAdapter Protocol ─────────────────────────────────────────────────────


@runtime_checkable
class BaseAdapter(Protocol):
    """Protocol that every build-engine adapter must satisfy.

    The Scanner and other core components depend only on this interface,
    never on a concrete adapter class, which keeps the Core engine-agnostic.
    """

    def is_locale_dir(self, part: str) -> bool:
        """Return ``True`` when *part* is a non-default locale directory name."""
        ...

    def resolve_asset(self, missing_abs: Path, docs_root: Path) -> Path | None:
        """Return the default-locale fallback for a missing asset, or ``None``."""
        ...

    def resolve_anchor(
        self,
        resolved_file: Path,
        anchor: str,
        anchors_cache: dict[Path, set[str]],
        docs_root: Path,
    ) -> bool:
        """Return ``True`` if an anchor miss should be suppressed via i18n fallback.

        When a file inside a locale sub-tree (e.g. ``docs/it/architecture.md``)
        does not contain the requested anchor — because headings are translated —
        this method checks whether the anchor exists in the corresponding
        default-locale file (e.g. ``docs/architecture.md``).  If it does, the
        ``AnchorMissing`` error is suppressed: MkDocs / Zensical will serve the
        default-locale page for this anchor at build time.

        Args:
            resolved_file: Absolute path of the locale file that was found but
                whose anchor set does not contain *anchor*.
            anchor: The fragment identifier that was not found (without ``#``).
            anchors_cache: Pre-built mapping of absolute ``Path`` → anchor slug
                set.  No disk I/O is performed — this is a pure in-memory check.
            docs_root: Resolved absolute ``docs/`` root (for path stripping).

        Returns:
            ``True`` if the anchor exists in the default-locale equivalent file
            and the error should be suppressed; ``False`` otherwise.
        """
        ...

    def is_shadow_of_nav_page(self, rel: Path, nav_paths: frozenset[str]) -> bool:
        """Return ``True`` when *rel* is a locale-mirror of a nav-listed page."""
        ...

    def get_ignored_patterns(self) -> set[str]:
        """Return filename glob patterns for non-default locale files (suffix mode)."""
        ...

    def get_nav_paths(self) -> frozenset[str]:
        """Return ``.md`` paths listed in the nav, relative to ``docs_root``."""
        ...

    def has_engine_config(self) -> bool:
        """Return ``True`` when a build-engine config was found and loaded.

        ``StandaloneAdapter`` returns ``False``.  All concrete adapters return
        ``True``.  Callers use this to decide whether a nav-based check
        (e.g. orphan detection) can produce meaningful results.
        """
        ...

    def get_metadata_files(self) -> frozenset[str]:
        """Return basenames of files consumed by this adapter as configuration.

        These files are Level 1b System Guardrails (CEO-050): automatically
        shielded from Z903 (Unused Asset) and other quality checks.  Return
        only plain filenames — no paths, no wildcards.
        """
        ...

    def map_url(self, rel: Path) -> str:
        """Map a physical source file path to its canonical virtual URL.

        **Legacy API** — retained for backward compatibility.  New adapters
        should implement ``get_route_info()`` instead; this method's default
        behaviour delegates to ``get_route_info().canonical_url``.

        Args:
            rel: Path of the Markdown source file, relative to ``docs_root``.

        Returns:
            Canonical URL string with leading and trailing slash
            (e.g. ``'/guide/installation/'``).
        """
        ...

    def classify_route(self, rel: Path, nav_paths: frozenset[str]) -> RouteStatus:
        """Classify a source file's routing status.

        **Legacy API** — retained for backward compatibility.  New adapters
        should implement ``get_route_info()`` instead; this method's default
        behaviour delegates to ``get_route_info().status``.

        Args:
            rel:       Path relative to ``docs_root``.
            nav_paths: Frozenset of nav-listed ``.md`` paths (from
                       ``get_nav_paths()``).

        Returns:
            ``RouteStatus`` literal: ``'REACHABLE'``, ``'ORPHAN_BUT_EXISTING'``,
            or ``'IGNORED'``.  ``'CONFLICT'`` is set later by
            ``_detect_collisions()`` and must not be returned here.
        """
        ...

    def get_route_info(self, rel: Path) -> RouteMetadata:
        """Return unified routing metadata for a source file.

        This is the **Metadata-Driven Routing** API introduced in v0.6.0a1.
        It centralises canonical URL computation, route classification, slug
        resolution, and proxy detection into a single call — eliminating the
        double-dispatch of ``map_url()`` + ``classify_route()`` and enabling
        eager metadata harvesting during VSM construction.

        Adapters that implement this method do not need to override
        ``map_url()`` or ``classify_route()`` separately — the VSM builder
        will prefer ``get_route_info()`` when available.

        Adapters that only implement the legacy ``map_url()`` +
        ``classify_route()`` surface are still supported: the VSM builder
        falls back to calling those methods when ``get_route_info`` is
        not overridden.

        Args:
            rel: Path of the source file relative to ``docs_root``.

        Returns:
            :class:`RouteMetadata` with all routing fields populated.
        """
        ...

    def provides_index(self, directory_path: Path) -> bool:
        """Return ``True`` when the engine generates a landing page for *directory_path*.

        Called once per discovered directory during the I/O discovery phase
        (never inside a per-link loop).  Implementations may call
        ``Path.exists()`` because this method is part of the I/O phase, not
        the hot-path rule engine.

        Conservative default: ``False``.  Adapters that generate directory
        index pages must override this method.

        Args:
            directory_path: Absolute path to the directory to check.

        Returns:
            ``True`` if the engine will serve a valid page at the directory
            URL (e.g. ``/docs/guides/``); ``False`` otherwise.
        """
        ...

    def get_link_scheme_bypasses(self) -> frozenset[str]:
        """Return URI scheme names this engine uses legitimately that must not be flagged.

        The validator adds a matching ``<scheme>:`` prefix to its skip list for
        each returned name, suppressing both the unknown-scheme error and the
        Z105 absolute-path check for URLs that use that scheme.

        The Core never names a specific engine — it queries the active adapter
        through this method, keeping the validator engine-agnostic.

        Example — Docusaurus ``pathname:///`` static-asset links::

            # DocusaurusAdapter
            def get_link_scheme_bypasses(self) -> frozenset[str]:
                return frozenset({"pathname"})

        Returns:
            A ``frozenset`` of scheme names (without the trailing colon), or
            ``frozenset()`` when the engine has no special link-scheme bypass.
        """
        ...

    def get_absolute_url_prefixes(self, repo_root: Path) -> frozenset[str]:
        """Return absolute URL prefixes the engine owns at the current site.

        Multi-instance engines (most notably Docusaurus with sibling
        ``@docusaurus/plugin-content-docs`` instances) route content trees
        under distinct top-level URL prefixes — e.g. ``/docs/`` for the user
        manual and ``/developers/`` for the developer area.  Cross-plugin
        links must be authored as absolute paths because the local VSM of
        either plugin cannot resolve the other plugin's relative tree.

        The Core honours **Zero-Config**: rather than asking the user to
        duplicate Docusaurus routing into ``zenzic.toml``, it queries the
        active adapter through this method.  Each returned prefix bypasses
        Z105 ``ABSOLUTE_PATH`` for URLs that start with it.

        Returned prefixes:

        - **MUST** start with ``/`` and **SHOULD** end with ``/`` (so
          ``/developers/`` matches ``/developers/intro`` but not
          ``/developers-only/``).
        - Are matched with ``str.startswith`` — engines that need richer
          semantics return them as multiple entries.

        Default implementation returns ``frozenset()`` so adapters that do
        not host multi-instance plugins continue to fire Z105 on every
        absolute internal link.

        Args:
            repo_root: Absolute repository root, supplied so the adapter can
                consult the filesystem (e.g. confirm a sibling content
                directory exists before claiming its prefix).

        Returns:
            A ``frozenset`` of project-owned absolute URL prefixes.
        """
        ...
